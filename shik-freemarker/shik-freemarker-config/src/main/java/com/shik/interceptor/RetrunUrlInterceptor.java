package com.shik.interceptor;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.web.servlet.HandlerInterceptor;
import org.springframework.web.servlet.ModelAndView;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 * @author gengshikun
 * @date 2017/9/1
 */
public class RetrunUrlInterceptor implements HandlerInterceptor {

    private static final Logger logger = LoggerFactory.getLogger(RetrunUrlInterceptor.class);

    @Override
    public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object obj) throws Exception {
        HttpSession session = request.getSession();
        session.setAttribute("returnUrl", this.getReturnUrl(request));
        return true;
    }

    /**
     * 获得returnUrl
     *
     * @param request
     * @return
     */
    private String getReturnUrl(HttpServletRequest request) {
        String currentUrl =
                request.getScheme() + "://" + request.getHeader("host")
                        + request.getContextPath()
                        + request.getServletPath()
                        + (null != request.getQueryString() ? "?" + request.getQueryString() : "");
        return currentUrl;
    }

    @Override
    public void postHandle(HttpServletRequest httpServletRequest, HttpServletResponse httpServletResponse, Object o, ModelAndView modelAndView) throws Exception {

    }

    @Override
    public void afterCompletion(HttpServletRequest httpServletRequest, HttpServletResponse httpServletResponse, Object o, Exception e) throws Exception {

    }
}
