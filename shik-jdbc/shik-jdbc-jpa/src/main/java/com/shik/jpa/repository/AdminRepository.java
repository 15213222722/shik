package com.shik.jpa.repository;

import com.shik.jpa.domain.Admin;
import com.shik.jpa.domain.Role;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.repository.PagingAndSortingRepository;

import java.util.List;

/**
 * @author gengshikun
 * @date 2017/7/3
 */
public interface AdminRepository extends PagingAndSortingRepository<Admin, String>, JpaRepository<Admin, String> {

    List<Admin> findByRoleList(List<Role> roles);

    @Select("select * from admin where id = #{id}")
    Admin getOne(@Param("id") String id);

    Admin findOneByUsername(String username);

}
