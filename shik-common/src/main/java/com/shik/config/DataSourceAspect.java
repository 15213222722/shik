package com.shik.config;

import com.shik.constant.enums.DataSourceType;
import com.shik.support.component.DynamicDataSource;
import org.aspectj.lang.annotation.Aspect;
import org.aspectj.lang.annotation.Before;
import org.aspectj.lang.annotation.Pointcut;
import org.springframework.context.annotation.Configuration;

/**
 * @author gengshikun
 * @date 2017/3/10
 */
@Aspect     //注解的方式配置aop
@Configuration
public class DataSourceAspect {
    @Pointcut("execution(* com.shik.*.service.*.*(..))")
    private void anyMethod() {
    }//定义一个切入点

    @Before("anyMethod()")
    public void dataSourceChange() {
        DynamicDataSource.setDataSource(DataSourceType.PRIMARY.getValue());
        /*这里根据用户的类型来更改对应的数据源*/
    }
}
