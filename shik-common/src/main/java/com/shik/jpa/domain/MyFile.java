package com.shik.jpa.domain;

import org.hibernate.annotations.GenericGenerator;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import java.io.Serializable;

/**
 * @author gengshikun
 * @date 2017/3/3
 */
@Entity(name = "`my_file`")
public class MyFile implements Serializable {

    @Id
    @GenericGenerator(name = "idGenerator", strategy = "uuid") //这个是hibernate的注解/生成32位UUID
    @GeneratedValue(generator = "idGenerator")
    @Column(length = 32)
    private String fileId;

    /**
     * 文件类型  SysConstents FILE_TYPE
     */
    @Column(length = 32)
    private String type;

    /**
     * 文件名
     */
    @Column(length = 32)
    private String name;

    /**
     * 文件大小
     */
    @Column
    private Long size;

    /**
     * 在服务器路径
     */
    @Column
    private String path;

    /**
     * 是否删除
     */
    @Column
    private Boolean deleteBoolean;

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getSize() {
        return size;
    }

    public void setSize(Long size) {
        this.size = size;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Boolean getDeleteBoolean() {
        return deleteBoolean;
    }

    public void setDeleteBoolean(Boolean deleteBoolean) {
        this.deleteBoolean = deleteBoolean;
    }
}
